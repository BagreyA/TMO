% Задание параметров
lambda = 3; % Интенсивность входного потока
mu = 5;     % Интенсивность обслуживания
rho = lambda / mu; % Коэффициент загрузки

% Аналитические характеристики показательного распределения
E_X = 1 / lambda;      % Математическое ожидание входного потока
D_X = 1 / (lambda^2); % Дисперсия входного потока

E_Y = 1 / mu;         % Математическое ожидание времени обслуживания
D_Y = 1 / (mu^2);     % Дисперсия времени обслуживания

% Параметры равномерного распределения
a = 0; % Нижняя граница
b = 1; % Верхняя граница

% Аналитические характеристики равномерного распределения
E_Z = (a + b) / 2;               % Математическое ожидание
D_Z = (b - a)^2 / 12;            % Дисперсия

% Задание длины последовательности
N = 200; % Длина последовательности

% Генерация управляющих последовательностей для M/M/1
j_n = exprnd(1/lambda, N, 1); % Показательное распределение для входного потока
v_n = exprnd(1/mu, N, 1);      % Показательное распределение для времени обслуживания

% Генерация управляющих последовательностей для равномерного распределения
j1_n = a + (b-a) * rand(N, 1); % Равномерное распределение для входного потока
v1_n = a + (b-a) * rand(N, 1); % Равномерное распределение для времени обслуживания

% Статистические характеристики для показательного распределения
M_j = mean(j_n);                       % Среднее
D_j = var(j_n);                        % Дисперсия

M_v = mean(v_n);                       % Среднее
D_v = var(v_n);                        % Дисперсия

% Статистические характеристики для равномерного распределения
M_j1 = mean(j1_n);                     % Среднее
D_j1 = var(j1_n);                      % Дисперсия

M_v1 = mean(v1_n);                     % Среднее
D_v1 = var(v1_n);                      % Дисперсия

% Проверка совпадения аналитических и статистических характеристик
fprintf('Аналитические и статистические характеристики для входного потока:\n');
fprintf('Математическое ожидание: %.4f (аналитическое), %.4f (статистическое)\n', E_X, M_j);
fprintf('Дисперсия: %.4f (аналитическое), %.4f (статистическое)\n\n', D_X, D_j);

fprintf('Аналитические и статистические характеристики для времени обслуживания:\n');
fprintf('Математическое ожидание: %.4f (аналитическое), %.4f (статистическое)\n', E_Y, M_v);
fprintf('Дисперсия: %.4f (аналитическое), %.4f (статистическое)\n\n', D_Y, D_v);

% Проверка статистических характеристик
fprintf('Сравнение статистических характеристик показательного и равномерного распределения:\n');
fprintf('Показательное распределение:\n');
fprintf('Математическое ожидание: %.4f, Дисперсия: %.4f\n', M_j, D_j);
